# Operadores de Comparación
# Igual a (==)
a = 10
b = 5
igual = a == b
print("Igual a:", igual)  # Output: Igual a: False

# No igual a (!=)
no_igual = a != b
print("No igual a:", no_igual)  # Output: No igual a: True

# Mayor que (>)
mayor_que = a > b
print("Mayor que:", mayor_que)  # Output: Mayor que: True

# Menor que (<)
menor_que = a < b
print("Menor que:", menor_que)  # Output: Menor que: False

# Mayor o igual que (>=)
mayor_o_igual = a >= b
print("Mayor o igual que:", mayor_o_igual)  # Output: Mayor o igual que: True

# Menor o igual que (<=)
menor_o_igual = a <= b
print("Menor o igual que:", menor_o_igual)  # Output: Menor o igual que: False